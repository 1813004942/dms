class UnboxNumericTester {
	private final byte unboxByte = 1;
	private final double unboxDouble = 1.0;
	private final float unboxFloat = 1.0f;
	private final int unboxInt = 1;
	private final long unboxLong = 1;
	private final short unboxShort = 1;

	/**
	 * @return {@link #unboxByte}
	 */
	public byte getUnboxByte() {
		return unboxByte;
	}

	/**
	 * @return {@link #unboxDouble}
	 */
	public double getUnboxDouble() {
		return unboxDouble;
	}

	/**
	 * @return {@link #unboxFloat}
	 */
	public float getUnboxFloat() {
		return unboxFloat;
	}

	/**
	 * @return {@link #unboxInt}
	 */
	public int getUnboxInt() {
		return unboxInt;
	}

	/**
	 * @return {@link #unboxLong}
	 */
	public long getUnboxLong() {
		return unboxLong;
	}

	/**
	 * @return {@link #unboxShort}
	 */
	public short getUnboxShort() {
		return unboxShort;
	}
}